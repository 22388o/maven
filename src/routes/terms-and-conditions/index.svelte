    <h3>Creation of Maven Items</h3>
    <p>
      All Token(s)”, “Creation(s)”, “Work”) posted on the Maven website is
      associated with a unique Liquid asset exclusively issued by an approved
      Maven Business. Each Maven Item (“Item(s)”, “Non-fungible token(s)”,
      “NFT(s)” or “Token”) is minted by a Business using the asset issuance
      functionality of the Liquid Network. The type of media files that are
      currently supported and can be used by Businesses in Maven to post their
      Hard Assets are jpg, png, gif and mp4. When a Business first uploads
      their Work to Maven, the media file is added to the IPFS network and
      assigned a unique content identifier (CID) derived from the SHA256 hash
      of the file. The Website hosts an IPFS node that is capable of
      maintaining a copy and serving the media files to other nodes.
      Businesses or anyone interested could run their own IPFS node to provide
      this service and add redundancy to the system. After adding the token to
      the IPFS network, the Business's wallet generates a Liquid asset
      issuance transaction containing some metadata including the title of the
      Items tokenized and the CID. This issuance transaction (“Mint”) is then
      broadcasted to be permanently recorded in the Liquid blockchain, added
      to the Blockstream Asset Registry and the minted Token is sent to the
      Business’s wallet that can then be traded in the Website. This action
      provides Maven Businesses and Purchasers with a permanent record of
      authenticity and ownership of the Token. Since the asset registry is an
      open source project publicly available on GitHub anyone interested could
      clone this project and provide redundancy to the system. After a Token
      is minted by a Business, he or she can then propose the terms (“Sale”)
      under which the Token will be sold to Purchasers (including the option
      to add royalties to be assigned to the original Business) in a primary
      sale. After a Maven Item is sold by the original Business, it can then
      be traded or resold within Maven by the Website to any Maven User.
    </p>
